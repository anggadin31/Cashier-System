    private void idProdukKeyPressed(java.awt.event.KeyEvent evt) {                                    
        // TODO add your handling code here:       
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String sql3 = "Select * from Produk where ID_Pr=?";
            try {
                PreparedStatement pst = con.prepareStatement(sql3);
                pst.setString(1, idProduk.getText());
                ResultSet rs = pst.executeQuery();
                if (rs.next()) {
                    String nama_Pr = rs.getString("nama_Pr");
                    String harga_Pr = rs.getString("harga_Pr");
                    namaProduk.setText(nama_Pr);
                    hargaProduk.setText(harga_Pr);
                }else{
                    JOptionPane.showMessageDialog(null, "Produk Tidak Tersedia");
                    namaProduk.setText(null);
                    hargaProduk.setText(null);
                }
            } catch (SQLException ex) {
                Logger.getLogger(transProduk.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }                                   

