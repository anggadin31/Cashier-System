    public double getSum() {
        int rowsCount = Transaksi.getRowCount();
        double sum = 0;
        for (int i = 0; i < rowsCount; i++) {
            sum += Double.parseDouble((Transaksi.getValueAt(i, 5).toString()));
        }
        return sum;
    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) Transaksi.getModel();
        String sql = "insert into penjualan_produk (ID_kasir, ID_Pr, nama_produk, harga_produk, jml_Pr)"
                + "values(?,?,?,?,?)";
        double subDiskon = Double.parseDouble(hargaProduk.getText()) * Double.parseDouble(jumlahProduk.getText())
                * (Double.parseDouble(diskon.getText()) / 100);
        double hasil = Double.parseDouble(hargaProduk.getText()) * Double.parseDouble(jumlahProduk.getText()) - subDiskon;
        model.addRow(new Object[]{idProduk.getText(), namaProduk.getText(), hargaProduk.getText(),
            jumlahProduk.getText(), diskon.getText(), hasil});
        PreparedStatement pst;
        try {
            pst = con.prepareStatement(sql);
            pst.setString(1, idKasir.getText());
            pst.setString(2, idProduk.getText());
            pst.setString(3, namaProduk.getText());
            pst.setString(4, hargaProduk.getText());
            pst.setString(5, jumlahProduk.getText());
            pst.execute();
            String sql2 = "update dbo.Produk set jml_Pr=produk.jml_Pr-" + jumlahProduk.getText() + "from dbo.penjualan_produk where dbo.Produk.ID_Pr='" + idProduk.getText() + "'";
            Statement pst2;
            pst2 = con.createStatement();
            pst2.addBatch(sql2);
            pst2.executeBatch();
            JOptionPane.showMessageDialog(null, "Success");
            clearFields();
        } catch (SQLException ex) {
            Logger.getLogger(transProduk.class.getName()).log(Level.SEVERE, null, ex);
        }
    }                                        

