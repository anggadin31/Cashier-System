    private void idJasaKeyPressed(java.awt.event.KeyEvent evt) {                                  
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String sql3 = "Select * from Jasa where ID_jasa=?";
            try {
                PreparedStatement pst = con.prepareStatement(sql3);
                pst.setString(1, idJasa.getText());
                ResultSet rs = pst.executeQuery();
                if (rs.next()) {
                    String nama_jasa = rs.getString("nama_jasa");
                    String harga_jasa = rs.getString("harga_jasa");
                    namaJasa.setText(nama_jasa);
                    hargaJasa.setText(harga_jasa);
                }else{
                    JOptionPane.showMessageDialog(null, "Jasa Tidak Tersedia");
                    namaJasa.setText(null);
                    hargaJasa.setText(null);
                }
            } catch (SQLException ex) {
                Logger.getLogger(transProduk.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }                                 

