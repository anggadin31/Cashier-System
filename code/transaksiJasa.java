    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) transaksi.getModel();
        String sql = "insert into penjualan_jasa (ID_kasir, ID_jasa, nama_jasa, harga_jasa)"
                + "values(?,?,?,?)";
        model.addRow(new Object[]{idJasa.getText(), namaJasa.getText(), hargaJasa.getText()});
        PreparedStatement pst;
        try {
            pst = con.prepareStatement(sql);
            pst.setString(1, idKasir.getText());
            pst.setString(2, idJasa.getText());
            pst.setString(3, namaJasa.getText());
            pst.setString(4, hargaJasa.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Success");
            clearFields();
        } catch (SQLException ex) {
            Logger.getLogger(transProduk.class.getName()).log(Level.SEVERE, null, ex);
        }
    }                                        

    public double getSum(){
        int rowsCount = transaksi.getRowCount();
        double sum = 0;
        for (int i = 0; i < rowsCount; i++) {
            sum += Double.parseDouble((transaksi.getValueAt(i, 2).toString()));
        }
        return sum;
    }
    
